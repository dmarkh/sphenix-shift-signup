<P STYLE="margin-left: 0.42in; margin-right: 0.5in"><A NAME="cqa1"></A>
The responsibilities of the Shift Crew include participating in
general data taking activities, and assisting the Shift Leader and
Detector Operator as required. A specific responsibility is updating
the sPHENIX check list and helping to monitor the run time indicators
for all systems to help insure quality operation of the detector
during data taking activities. This includes monitoring the OnlineQA
plots which are accumulated online and proactively analyzing and
examining in real time (or as close to it as possible) a small sample
of the data being taken using a standard program incorporating the
full sPHENIX software chain operating as part of fast-offline QA. The
intent is to monitor key reconstructed physics observables which
indicate the quality of the data being taken. Possible examples
include the width of reconstructed mass peaks, reconstructed vertex
distributions, multiplicity distributions, scaler ratios versus time,
etc. These indicators will be monitored for stability for a given
running configuration, and to assess the stability of DAQ1000 data
versus data taken periodically with fully written cluster
information.</P>
