<P STYLE="margin-left: 0.42in; margin-right: 0.5in"><A NAME="do1"></A>
A sPHENIX Detector Operator is responsible for insuring all sPHENIX
detectors are operated in accordance with their safe operational
envelope. He/she is responsible for monitoring slow controls
information regarding detector performance, and for taking
appropriate action to turn detectors on and off when called for by
the condition of the accelerator or of the beams during the course of
an 8 hour shift. He/she is responsible for having a detailed
knowledge of the safe operational envelope of all sPHENIX detectors, and
is recognized as a resource and an authority in this area. With
regard to the issue of &quot;turning on&quot; or energizing sPHENIX
detector components, the Shift Leader and the Detector Operator both
play a role. The decision to turn on a detector requires the
agreement of both the Detector Operator and the Shift Leader. If
either person believes the detector should remain off for the safety
of the subsystem, the detector system will continue to remain in a
state in which it is not energized. Either person can decide to turn
a detector system off if they feel continued operation would result
in an unsafe condition for the detector subsystem.</P>
<P STYLE="margin-left: 0.42in; margin-right: 0.5in; margin-top: 0.07in;
margin-bottom: 0.07in"><A NAME="do2"></A>
A Detector Operator comes from a member institution of the sPHENIX
Collaboration and usually serves a minimum of two seven-day shift
periods plus 1 overlap day during a given sPHENIX-RHIC running period.
Detector Operators are identified by sPHENIX Management with input from
the Collaboration, trained according to sPHENIX procedures, and reviewed
and recommended for certification by the sPHENIX Shift Leader Selection
Quality Assurance Board. Certification of sPHENIX Detector Operators is
by the sPHENIX Spokesperson, upon recommendation by the sPHENIX Quality
Assurance Board.</P>
