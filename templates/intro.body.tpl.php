<H1 ALIGN=CENTER>General Instructions and Welcome</H1>
<P STYLE="margin-left: 0.42in">Welcome to the sPHENIX shift signup web
page. Participating in shift work is a great way to learn about the
operation and capabilities of the sPHENIX detector, and to contribute to
sPHENIX's scientific program. Your participation is greatly appreciated!</P>

