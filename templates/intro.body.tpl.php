<P STYLE="margin-left: 0.42in; margin-right: 0.5in"><A NAME="sl1"></A>
A sPHENIX Shift Leader directs the running of the sPHENIX experiment during
a shift of approximately 8 hours, once a day, for a minimum period of
eight days. He/she is the only person recognized by the
Collider-Accelerator Department as being part of the C-AD Conduct of
Operations. He/she is responsible to the Chairman of C-AD, through
the C-AD Operations Coordinator, for insuring safe operation of the
sPHENIX Detector during normal data taking activities, fully within the
operational envelope established by C-AD and the BNL Standards Based
Management System.</P>
<P STYLE="margin-left: 0.42in; margin-right: 0.5in; margin-top: 0.07in;
margin-bottom: 0.07in"><A NAME="sl2"></A>
The Shift Leader guides the Shift Crew, maintains liaison with the
RHIC Main Control Room and contacts the subsystem experts if problems
arise in the course of sPHENIX operations. The goal of his/her
leadership and direction is to achieve the near term scientific goals
of the sPHENIX program during an 8 hour period. A Shift Leader has final
authority on the minute to minute conduct of sPHENIX data taking
activities in the sPHENIX Control room, with one exception. With regard
to the issue of &quot;turning on&quot; or energizing sPHENIX detector
components, the Shift Leader and the <A HREF="#do"><B>Detector
Operator</B></A> both play a role. The decision to turn on a detector
requires the agreement of both the Detector Operator and the Shift
Leader. Either person can decide to turn a detector system off if
they feel continued operation would result in an unsafe condition for
the detector subsystem.
</P>
<P STYLE="margin-left: 0.42in; margin-right: 0.5in">An important
aspect of this responsibility is to insure that at the end of each
run, an entry is made in the run log by someone on shift to indicate
that the run is:
</P>
<OL>
	<LI><P STYLE="margin-bottom: 0in">free of any known problems
	</P>
	<LI><P STYLE="margin-bottom: 0in">probably good, although some
	unusual circumstance occurred; it should be examined before
	inclusion in analysis
	</P>
	<LI><P>likely to have problems; it should not be included in
	analysis without detailed examination
	</P>
</OL>
<P STYLE="margin-left: 0.42in; margin-right: 0.5in; margin-top: 0.07in;
margin-bottom: 0.07in">
A Shift Leader comes from a member institution of the sPHENIX
Collaboration and usually serves a minimum of two seven-day shift
periods plus 1 overlap day during a given sPHENIX-RHIC running period.
Shift Leader candidates are identified and trained according to sPHENIX
procedures, and are recommended to the sPHENIX Spokesperson for
certification by the sPHENIX Shift Leader Selection Quality Assurance
Board. Certification of sPHENIX Shift Leaders is made by the Chairman of
the Collider-Accelerator Department, upon recommendation by the sPHENIX
Spokesperson.
