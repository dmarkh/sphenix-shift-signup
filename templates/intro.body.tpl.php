<P STYLE="margin-left: 0.42in"><A NAME="pc1"></A>
The individual, appointed and reporting to the sPHENIX Spokesperson Office for a certain period during a RHIC run, 
has responsibility for oversight and continuity of the sPHENIX experimental scientific program on day-to-day, 
week-to-week basis. 
</P>
<P STYLE="margin-left: 0.42in"><A NAME="pc1"></A>
Once the sPHENIX detector has been commissioned and brought into "routine" operation for data taking, 
the Period Coordinator is responsible for calling and leading meetings required for the coordination of regular shift crew in carrying out the near term scientific program. The period coordinator should contact each shift once a day at least to ensure that the information flow between shifts is correct. He/she is expected to attend weekly trigger-board meetings to be updated on any decision/change on the near-term run program, to attend fast-offline QA meetings to coordinate with offline on calibration requests, to chair daily sPHENIX operation meetings, and to attend the regular CAD operation meetings, weekly scheduling meetings and machine/experiment coordination meetings to understand and convey CAD plans to sPHENIX operation and shift team, to give a regular presentation of sPHENIX run progress and updates at the weekly CAD time meeting. The period coordinator will be in close contact with the sPHENIX operation management team on issues related to the detector subsystems, shifts, operation procedure and CAD schedule/plan. 
Should a serious technical problem arise, which cannot be fixed simply by members of the shift crew in consultation with Detector Experts, 
the sPHENIX Operations Coordinator will assume responsibility for coordinating the effort by Detector Experts to bring the detector back into normal operation. 
Once the normal operation is restored, the Period Coordinator will resume responsibility for oversight and coordination of Shift Leaders and Shift crew to take sPHENIX data.
</P>
<P STYLE="margin-left: 0.42in">
The Period Coordinator is not on shift during his/her tour, but he/she will be available, on 24-hour call, for consultation with the sPHENIX Shift Leaders concerning ongoing data taking priorities and pursuit of the sPHENIX scientific program. The Period Coordinator is from a member institution of the sPHENIX Collaboration. He/She has a good knowledge of the near term goals of the sPHENIX scientific program, as well as a working understanding of the configuration and operation of sPHENIX detectors systems (in particular the impact of various trigger configurations) in order to consult effectively on near term strategies for the effective pursuit of sPHENIX's scientific goals.  A period of two consecutive weeks for serving as a Period Coordinator is highly preferred but not required.
</P>
