<P STYLE="margin-left: 0.42in; margin-right: 0.5in; margin-top: 0.07in;
margin-bottom: 0.07in"><A NAME="oqa1"></A>
In addition to personnel present at the sPHENIX detector site during
data taking an additional person will be on duty for 1 shift per day
(day shift) to review the output of offline quality assurance
production. The purpose of this effort is to provide quality
assurance evaluation of sPHENIX data production for both &quot;real
time&quot; data from the ongoing experiment run (fast-offline QA) and
for final DST production of data acquired during previous runs or
much earlier in the present run (real data production QA).
</P>
<P STYLE="margin-left: 0.42in; margin-right: 0.5in; margin-top: 0.07in;
margin-bottom: 0.07in"><A NAME="oqa2"></A>
The person on this shift will be responsible for regular completion
of a number of web based tasks. In addition, they will be responsible
for reviewing the reports of the Quality Assurance Monitor and Real
Time Assistant (e.g. in the run log) to establish a list of quality
runs thought to be without problem as sequential progress is made
through the data archive. At the end of the run, one result of this
effort will be a comprehensive list based on the individual reports
of all the Offline Quality Assurance Monitor of those runs which
should (and those which should not) be included in a production run
for data analysis. Once DST production is running at full capacity
the offline QA shift will be very demanding and will probably require
the full 8 hours to complete. &nbsp;</P>
<P STYLE="margin-left: 0.42in; margin-right: 0.5in; margin-top: 0.07in;
margin-bottom: 0.07in"><A NAME="oqa3"></A>
Personnel who take responsibility for offline QA shifts are not
required to be present in the counting house. However, they are
required to be on site at BNL for their first week of Offline QA
shift service. Subsequent offline QA shifts may be done from non-BNL
sites provided adequate web access to the Auto QA system can be
demonstrated from the remote site.</P>
<P STYLE="margin-left: 0.42in"><A NAME="oqa4"></A>Presence at the BNL
site initially is motivated by the fact that many unforeseen problems
with the quality assurance/production effort will very likely arise
during these shifts and quick, real time help, which is more likely
to be available at BNL, is essential to insure daily shift
productivity.
</P>
